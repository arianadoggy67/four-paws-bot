# ========== ФУНКЦИЯ ДЛЯ ПОИСКА ЗНАЧЕНИЯ ПО КЛЮЧУ С ПРОБЕЛАМИ ==========
def get_val(строка, ключ):
    for k, v in строка.items():
        if k.strip() == ключ.strip():
            return v
    for k, v in строка.items():
        if ключ.strip().lower() in k.strip().lower():
            return v
    return ""

