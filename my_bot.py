@bot.message_handler(func=lambda m: True)
def handle_message(message):
    if not sheet:
        bot.reply_to(message, "База временно недоступна.", reply_markup=get_main_keyboard())
        return
    
    текст = message.text.strip()
    
    # Если есть запятые — пробуем полный запрос
    if "," in текст:
        части = [p.strip() for p in текст.split(",")]
        
        if len(части) >= 6:
            кличка = части[0]
            порода = части[1]
            try:
                вес = float(части[2].replace("кг", "").replace("г", "").strip())
            except:
                bot.reply_to(message, "Вес должен быть числом. Пример: Чарли, чихуахуа, 3, 3 года, да, яблоко", reply_markup=get_main_keyboard())
                return
            возраст = части[3]
            стерилизована = "да" in части[4].lower() or "yes" in части[4].lower()
            продукт = части[5]
            
            средний_вес_породы, порода_название = получить_средний_вес(порода)
            строка = найти_продукт(продукт)
            
            if строка:
                ответ = рассчитать_дозу(строка, вес, возраст, стерилизована, средний_вес_породы, порода_название)
                ответ = ответ.replace(get_val(строка, "Можно_нельзя"), f"{кличке} можно {продукт}!")
                bot.reply_to(message, ответ, reply_markup=get_main_keyboard())
                return
        
        elif len(части) >= 5:
            кличка = части[0]
            try:
                вес = float(части[1].replace("кг", "").strip())
            except:
                bot.reply_to(message, "Вес должен быть числом.", reply_markup=get_main_keyboard())
                return
            возраст = части[2]
            стерилизована = "да" in части[3].lower() or "yes" in части[3].lower()
            продукт = части[4]
            
            строка = найти_продукт(продукт)
            
            if строка:
                ответ = рассчитать_дозу(строка, вес, возраст, стерилизована, СРЕДНИЙ_ВЕС_ЧИХУАХУА, "чихуахуа")
                ответ = ответ.replace(get_val(строка, "Можно_нельзя"), f"{кличке} можно {продукт}!")
                bot.reply_to(message, ответ, reply_markup=get_main_keyboard())
                return
    
    # Простой поиск продукта
    строка = найти_продукт(текст)
    if строка:
        ответ = рассчитать_дозу(строка, СРЕДНИЙ_ВЕС_ЧИХУАХУА, "взрослая", False, СРЕДНИЙ_ВЕС_ЧИХУАХУА, "чихуахуа")
        ответ = ответ.replace(f"📊 Разовая порция: {round(float(get_val(строка, 'Норма_г') or 0))} г", f"📊 Базовая норма: {get_val(строка, 'Норма_г')} г")
        bot.reply_to(message, ответ, reply_markup=get_main_keyboard())
        return
    
    # Ничего не нашли — пересылаем тебе
    user_info = f"📩 Сообщение от @{message.from_user.username or 'без ника'} (ID: {message.from_user.id}):\n\n{message.text}"
    try:
        bot.send_message(YOUR_TELEGRAM_ID, user_info)
    except:
        pass
    bot.reply_to(message, "Спасибо! Ариана получила твой вопрос и скоро ответит ❤️", reply_markup=get_main_keyboard())

