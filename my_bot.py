def найти_продукт(продукт):
    if not sheet:
        return None
    данные = sheet.get_all_records()
    продукт = продукт.lower().strip()
    
    for строка in данные:
        синонимы = get_val(строка, "Синонимы").lower().strip()
        if not синонимы:
            continue
        список_синонимов = [s.strip().lower() for s in синонимы.split(",")]
        if продукт in список_синонимов:
            return строка
    
    for строка in данные:
        название = get_val(строка, "Продукт").lower().strip()
        if продукт == название:
            return строка
    
    return None

